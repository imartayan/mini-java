class ArrayTest {
    public static void main(String[] args) {
        int[] t;

        // Array Allocation
        t = new int[10];

        // Array Assignment
        t[0] = 2;
        t[1] = 3;

        // Array Lookup
        System.out.println((t[0]) + (t[1]));

        // Array Length
        System.out.println(t.length);
    }
}
